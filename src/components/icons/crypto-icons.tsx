// ─── Public component ─────────────────────────────────────────────────────────

interface CryptoIconProps {
  symbol: string;
  size?: number;
  className?: string;
}

/**
 * Renders a crypto logo with official brand color background.
 * Falls back to first letter for unknown symbols.
 */
export function CryptoIcon({ symbol, size = 32, className }: CryptoIconProps) {
  const Icon = CRYPTO_ICON_MAP[symbol];
  const innerSize = Math.round(size * 0.55);

  if (Icon) {
    return (
      <Icon
        width={innerSize}
        height={innerSize}
        className={className}
        style={{ color: "white" }}
      />
    );
  }

  // Fallback: first letter
  return (
    <span
      className="font-bold text-white"
      style={{ fontSize: Math.round(size * 0.38) }}
    >
      {symbol.slice(0, 1)}
    </span>
  );
}

export { CRYPTO_ICON_MAP };
